     Ctrl — справа и слева. Открывает дополнительные возможности программ.

     Nut Look — при включённом индикаторе Nut Look – работает цифровая (числовая) клавиатура, которая расположена на клавиатуре либо отдельным блоком, справа, либо в центре, на клавишах –алфавитно-цифровых.

        Enter — клавиша ввода информации, подтверждает команду «да». Например: вы вводите в адресную строку браузера какой-либо адрес, но никакой кнопки «найти» там нет, поэтому нажимаем клавишу Enter, тем самым давая команду браузеру – найти. В поисковых  строках также можно не нажимать «найти», а нажать Enter. И при переходе на следующую строку при печатании – также, нажимаем Ente
